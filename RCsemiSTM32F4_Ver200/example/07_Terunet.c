/* ↓↓　以下がサンプル　↓↓ */
/******************************/
#include <stm32f4xx.h>	// **最初にインクルードする
#include "terunet.h"
#include "timer.h"
#include "terunet_motorDriver.h"

int main(void)
{
	/* -------------	変数宣言	----------------- */
	u8 recv[10];
	int tmp = 50;
	/* ---------------------------------------------- */

	/* -----------------	初期化	----------------- */
	SystemCoreClockUpdate();	// **最初に呼び出す
	TIM7_CMT_Init();
	Terunet_Init(TERUNET_BAUDRATE);

	/* ---------------------------------------------- */

	/* -------------	メインループ	------------- */
	while(1)
	{
		tmp = 50 - tmp;
		Terunet_MD_Drive(0x02, 0, tmp, recv);
		delay_ms(100);
	}
	/* ---------------------------------------------- */

	return 0;
}
